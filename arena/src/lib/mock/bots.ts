function generateSparkline(trend: 'up' | 'down' | 'flat', volatility: number): number[] {
  const points: number[] = [];
  let value = 100;
  for (let i = 0; i < 30; i++) {
    const drift = trend === 'up' ? 0.5 : trend === 'down' ? -0.3 : 0.05;
    value += drift + (Math.random() - 0.5) * volatility;
    points.push(Math.round(value * 100) / 100);
  }
  return points;
}

